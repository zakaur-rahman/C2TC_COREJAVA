package com.c2tc.loops;

public class IfElseIfExample {
	public static void main(String[] args) {
		int a = 2;
		if (a == 1) {
			System.out.println("one");
		} else if (a == 2) {
			System.out.println("Two");
		} else if (a == 3) {
			System.out.println("Three");
		} else {
			System.out.println("invalid");
		}
	}

}